package com.binaryelysium.mp3tunes;

public class Artist {
	int mId;
	String mName; 
	int mTrackCount;
	int mAlbumCount;
	int mSize;

	public int getId() {
		return mId;
	}
	public String getName() {
		return mName;
	}
	public int getTrackCount() {
		return mTrackCount;
	}
	public int getAlbumCount() {
		return mAlbumCount;
	}
	public int getSize() {
		return mSize;
	}
	
}
